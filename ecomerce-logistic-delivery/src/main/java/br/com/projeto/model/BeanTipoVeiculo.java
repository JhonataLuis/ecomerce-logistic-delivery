package br.com.javabean.bean;

/**
 *
 * @author rayss
 */
public class BeanTipoVeiculo {
    
    private int codTipoVeiculo;
    private String tipoVeiculo;
    private String marcaVeiculo;
    private String modeloVeiculo;
    private int anoVeiculo;

    /**
     * @return the codTipoVeiculo
     */
    public int getCodTipoVeiculo() {
        return codTipoVeiculo;
    }

    /**
     * @param codTipoVeiculo the codTipoVeiculo to set
     */
    public void setCodTipoVeiculo(int codTipoVeiculo) {
        this.codTipoVeiculo = codTipoVeiculo;
    }

    /**
     * @return the tipoVeiculo
     */
    public String getTipoVeiculo() {
        return tipoVeiculo;
    }

    /**
     * @param tipoVeiculo the tipoVeiculo to set
     */
    public void setTipoVeiculo(String tipoVeiculo) {
        this.tipoVeiculo = tipoVeiculo;
    }

    /**
     * @return the marcaVeiculo
     */
    public String getMarcaVeiculo() {
        return marcaVeiculo;
    }

    /**
     * @param marcaVeiculo the marcaVeiculo to set
     */
    public void setMarcaVeiculo(String marcaVeiculo) {
        this.marcaVeiculo = marcaVeiculo;
    }

    /**
     * @return the modeloVeiculo
     */
    public String getModeloVeiculo() {
        return modeloVeiculo;
    }

    /**
     * @param modeloVeiculo the modeloVeiculo to set
     */
    public void setModeloVeiculo(String modeloVeiculo) {
        this.modeloVeiculo = modeloVeiculo;
    }

    /**
     * @return the anoVeiculo
     */
    public int getAnoVeiculo() {
        return anoVeiculo;
    }

    /**
     * @param anoVeiculo the anoVeiculo to set
     */
    public void setAnoVeiculo(int anoVeiculo) {
        this.anoVeiculo = anoVeiculo;
    }
}
