package br.com.projeto.model;

/**
 *
 * @author rayss
 */
public class BeanProducts {
    
    private int idProduct;
    private int codParceVend;
    private String loginUsuEmp;
    private String nomeProduct;
    private String descProduct;
    private String catProduct;
    private float precoProduct;
    private String detalProduct;
    private String imgProduct;

    /**
     * @return the idProduct
     */
    public int getIdProduct() {
        return idProduct;
    }

    /**
     * @param idProduct the idProduct to set
     */
    public void setIdProduct(int idProduct) {
        this.idProduct = idProduct;
    }

    /**
     * @return the nomeProduct
     */
    public String getNomeProduct() {
        return nomeProduct;
    }

    /**
     * @param nomeProduct the nomeProduct to set
     */
    public void setNomeProduct(String nomeProduct) {
        this.nomeProduct = nomeProduct;
    }

    /**
     * @return the descProduct
     */
    public String getDescProduct() {
        return descProduct;
    }

    /**
     * @param descProduct the descProduct to set
     */
    public void setDescProduct(String descProduct) {
        this.descProduct = descProduct;
    }

    /**
     * @return the catProduct
     */
    public String getCatProduct() {
        return catProduct;
    }

    /**
     * @param catProduct the catProduct to set
     */
    public void setCatProduct(String catProduct) {
        this.catProduct = catProduct;
    }

    /**
     * @return the precoProduct
     */
    public float getPrecoProduct() {
        return precoProduct;
    }

    /**
     * @param precoProduct the precoProduct to set
     */
    public void setPrecoProduct(float precoProduct) {
        this.precoProduct = precoProduct;
    }

    /**
     * @return the detalProduct
     */
    public String getDetalProduct() {
        return detalProduct;
    }

    /**
     * @param detalProduct the detalProduct to set
     */
    public void setDetalProduct(String detalProduct) {
        this.detalProduct = detalProduct;
    }

    /**
     * @return the imgProduct
     */
    public String getImgProduct() {
        return imgProduct;
    }

    /**
     * @param imgProduct the imgProduct to set
     */
    public void setImgProduct(String imgProduct) {
        this.imgProduct = imgProduct;
    }

    /**
     * @return the loginUsuEmp
     */
    public String getLoginUsuEmp() {
        return loginUsuEmp;
    }

    /**
     * @param loginUsuEmp the loginUsuEmp to set
     */
    public void setLoginUsuEmp(String loginUsuEmp) {
        this.loginUsuEmp = loginUsuEmp;
    }

    /**
     * @return the codParceVend
     */
    public int getCodParceVend() {
        return codParceVend;
    }

    /**
     * @param codParceVend the codParceVend to set
     */
    public void setCodParceVend(int codParceVend) {
        this.codParceVend = codParceVend;
    }

   
    
    
}
